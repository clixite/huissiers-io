let currentLang = 'fr';

function detectLanguage() {
  const stored = localStorage.getItem('huissiers-lang');
  if (stored && ['fr', 'nl', 'en'].includes(stored)) return stored;
  const nav = (navigator.language || navigator.userLanguage || 'fr').toLowerCase();
  if (nav.startsWith('nl')) return 'nl';
  if (nav.startsWith('en')) return 'en';
  return 'fr';
}

function setLang(lang) {
  if (!['fr', 'nl', 'en'].includes(lang)) return;
  currentLang = lang;
  localStorage.setItem('huissiers-lang', lang);
  applyTranslations(lang);
}

function applyTranslations(lang) {
  // Update html lang
  const langMap = { fr: 'fr-BE', nl: 'nl-BE', en: 'en' };
  document.documentElement.lang = langMap[lang];

  // Update page title & meta description
  document.title = translations.pageTitle[lang];
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute('content', translations.pageDescription[lang]);

  // Update text-only elements
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (translations[key] && translations[key][lang]) {
      el.textContent = translations[key][lang];
    }
  });

  // Update elements containing trusted static HTML (authored by the developer, not user-generated)
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-html');
    if (translations[key] && translations[key][lang]) {
      // Safe: all values in the translations object are static developer-authored strings
      el.innerHTML = translations[key][lang];
    }
  });

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-placeholder');
    if (translations[key] && translations[key][lang]) {
      el.placeholder = translations[key][lang];
    }
  });

  // Update lang switcher active states
  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    var btnLang = btn.getAttribute('data-lang');
    if (btnLang === lang) {
      btn.classList.add('text-white', 'bg-white/10');
      btn.classList.remove('text-gray-400');
    } else {
      btn.classList.remove('text-white', 'bg-white/10');
      btn.classList.add('text-gray-400');
    }
  });

  // Update form button text (in case form was submitted)
  var submitBtn = document.querySelector('#contact-form button[type="submit"]');
  if (submitBtn && !submitBtn.disabled) {
    submitBtn.textContent = translations.formSubmit[lang];
  }
}

